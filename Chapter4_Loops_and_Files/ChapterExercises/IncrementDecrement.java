/** 
 * This program demonstrate the ++ and -- operators.
 */
package com.mycompany.loops_and_files; //DO NOT USE THIS LINE

public class IncrementDecrement 
{
    public static void main(String[] args)
    {
        int number = 4;
        
        //Display the value in number
        System.out.println("number is " + number);
        System.out.println("I will increment number.");
        
        //Increment number
        number++;
        
        System.out.println("Now, number is " + number);
        System.out.println("I will decrement number.");
        
        //Decrement number
        number--;
        
        System.out.println("Now, number is " + number);      
     }
}
